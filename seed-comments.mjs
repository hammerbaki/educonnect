import mysql from "mysql2/promise";

const DATABASE_URL = process.env.DATABASE_URL;
if (!DATABASE_URL) {
  console.error("DATABASE_URL not set");
  process.exit(1);
}

const connection = await mysql.createConnection(DATABASE_URL);

// 1. 기존 댓글 전부 삭제 (깨끗하게 시작)
await connection.execute(`DELETE FROM community_comments`);
console.log("Cleared all existing comments.");

// 2. 유저 ID 매핑
const [userRows] = await connection.execute(`SELECT id, openId, name FROM users WHERE openId LIKE 'dummy_user_%' ORDER BY id`);
const userMap = {};
for (const u of userRows) {
  userMap[u.openId] = { id: u.id, name: u.name };
}
console.log(`Found ${Object.keys(userMap).length} dummy users`);

// 3. 게시글 ID 매핑
const [postRows] = await connection.execute(`SELECT id, title, category FROM community_posts ORDER BY id`);
const postMap = {};
for (const p of postRows) {
  postMap[p.title] = { id: p.id, category: p.category };
}
console.log(`Found ${Object.keys(postMap).length} posts`);

