// 5. 댓글 삽입 및 commentCount 동기화
let totalInserted = 0;
let postsUpdated = 0;

for (const [title, comments] of Object.entries(commentsData)) {
  const post = postMap[title];
  if (!post) {
    console.log(`⚠️ Post not found: "${title}"`);
    continue;
  }

  const postId = post.id;

  // 댓글 삽입
  for (let i = 0; i < comments.length; i++) {
    const c = comments[i];
    const createdAt = new Date(Date.now() - (comments.length - i) * 300000); // 5분 간격
    await connection.execute(
      `INSERT INTO community_comments (postId, userId, authorName, content, createdAt, updatedAt) VALUES (?, ?, ?, ?, ?, ?)`,
      [postId, c.userId, c.authorName, c.content, createdAt, createdAt]
    );
    totalInserted++;
  }

  // commentCount 업데이트 - 실제 댓글 수로 동기화
  const [countResult] = await connection.execute(
    `SELECT COUNT(*) as cnt FROM community_comments WHERE postId = ?`,
    [postId]
  );
  const realCount = countResult[0].cnt;
  await connection.execute(
    `UPDATE community_posts SET commentCount = ? WHERE id = ?`,
    [realCount, postId]
  );
  postsUpdated++;
  console.log(`✅ "${title}" → ${realCount} comments`);
}

// 6. 댓글이 없는 게시글의 commentCount도 0으로 동기화
const [allPosts] = await connection.execute(`SELECT id, title FROM community_posts`);
for (const p of allPosts) {
  const [countResult] = await connection.execute(
    `SELECT COUNT(*) as cnt FROM community_comments WHERE postId = ?`,
    [p.id]
  );
  const realCount = countResult[0].cnt;
  await connection.execute(
    `UPDATE community_posts SET commentCount = ? WHERE id = ?`,
    [realCount, p.id]
  );
}

console.log(`\n🎉 Done! Inserted ${totalInserted} comments across ${postsUpdated} posts.`);
console.log("All commentCount values synced with actual comment counts.");

await connection.end();
